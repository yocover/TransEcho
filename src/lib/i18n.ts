export function detectLocale(): Locale {
  const lang = navigator.language.toLowerCase().split("-")[0];
  const supported: Locale[] = ["zh", "en", "ja", "de", "fr", "es", "pt", "id"];
  return supported.includes(lang as Locale) ? (lang as Locale) : "zh";
}

export function t(locale: Locale, key: string): string {
  return messages[locale]?.[key] ?? messages.zh[key] ?? key;
}

/**
 * Translate a backend status/error message key to the current locale.
 * Handles plain keys ("stopped") and keyed details ("api_failed:some detail").
 */
export function translateStatus(locale: Locale, message: string): string {
  // Direct key match
  const direct = messages[locale]?.[message];
  if (direct && !direct.includes("{0}")) return direct;

  // Pattern: "key:detail" for parameterized messages
  const idx = message.indexOf(":");
  if (idx > 0) {
    const key = message.substring(0, idx);
    const detail = message.substring(idx + 1);
    const template = messages[locale]?.[key];
    if (template) return template.replace("{0}", detail);
  }

  return message;
}
